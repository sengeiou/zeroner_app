package com.iwown.sport_module.model;

public class Po {
    int R;
    long T;
    int V;
    double X;
    double Y;

    public int getR() {
        return this.R;
    }

    public void setR(int r) {
        this.R = r;
    }

    public long getT() {
        return this.T;
    }

    public void setT(long t) {
        this.T = t;
    }

    public int getV() {
        return this.V;
    }

    public void setV(int v) {
        this.V = v;
    }

    public double getX() {
        return this.X;
    }

    public void setX(double x) {
        this.X = x;
    }

    public double getY() {
        return this.Y;
    }

    public void setY(double y) {
        this.Y = y;
    }
}
